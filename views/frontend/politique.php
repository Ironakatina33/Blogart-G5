<p>
Introduction:
</p>
<p>
Notre blog consacré à l'histoire médiévale respecte la vie privée de nos utilisateurs et s'engage à protéger leurs données personnelles conformément au Règlement général sur la protection des données (RGPD) de l'Union européenne. Cette politique explique comment nous collectons, utilisons, partageons et protégeons vos informations personnelles.
</p>
<p>
</p>
<p>
</p>
<p>
Collecte de données:
</p>
<p>
Nous collectons les informations suivantes de nos utilisateurs : nom, adresse e-mail, numéro de téléphone et adresse postale lorsqu'ils s'inscrivent à notre newsletter ou créent un compte sur notre site.
</p>
<p>
Utilisation des données:
</p>
<p>
Nous utilisons les informations collectées pour les finalités suivantes :
    </p>

    <p>
    Envoyer des newsletters et des informations sur les nouveaux articles et mises à jour du blog
Améliorer l'expérience utilisateur sur notre site en personnalisant le contenu et les offres
Analyser les données pour améliorer les performances de notre site
Partage de données:
Nous ne vendons ni ne partageons les informations personnelles de nos utilisateurs avec des tiers sans leur consentement explicite. Nous pouvons cependant être obligés de divulguer des informations si la loi l'exige.
</p>
<p>
Sécurité des données:
</p>
<p>
Nous prenons des mesures de sécurité pour protéger les informations personnelles de nos utilisateurs contre la perte, l'utilisation abusive, l'accès non autorisé, la divulgation et la modification.
</p>

<p>
Droits des utilisateurs:
</p>
<p>
Les utilisateurs peuvent accéder, mettre à jour ou supprimer leurs informations personnelles en contactant notre équipe de support. Ils peuvent également demander de ne plus recevoir nos newsletters en cliquant sur le lien de désabonnement présent dans chaque email.
Modifications de la politique de confidentialité:
Nous nous réservons le droit de modifier cette politique de temps à autre pour tenir compte des évolutions réglementaires ou de notre pratique en matière de protection des données. Les modifications seront publiées sur cette page.
</p>
